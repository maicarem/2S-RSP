function debug_subtour(filename, x_hat, y_hat, status)
    file = open("debug/$(filename).txt", "w")
    if status == 1
        println(file, "Ring structure not containing subtour")
        println(file, contain_subtour_bug(x_hat, y_hat))
    elseif status == 0
        println(file, "Ring structure contains subtour")
        println(file, contain_subtour_bug(x_hat, y_hat))
        for i in 1:n
            for j in 1:n
                if x_hat[i,j] == 1
                    println("Node $(i) --> $(j)")
                end
            end
        end

    end   
end

function cal_obj_sp0(alpha, beta, x_hat)
    if length(V_tilt) == 1
        return sum((x_hat[i,j]+ x_hat[j,k] - 1)* beta[i,j,k] for (i,j,k) in K_tilt)
    elseif length(V_tilt) == 0
        return 0
    else
        return sum((x_hat[k,i] + x_hat[i,j]+x_hat[j,t] - 2) * alpha[i,j,k,t] for (i,j,k,t) in J_tilt) + sum((x_hat[i,j]+ x_hat[j,k] - 1)* beta[i,j,k] for (i,j,k) in K_tilt)
    end
end


